    <h1>Anxiety</h1>
    <p>Anxiety disorders are serious mental illnesses that cause significant worry or fear that doesn't go away and may even get worse over time. We all feel anxious at times, but with an anxiety disorder, the anxiety tends to be fairly constant and has a very negative and intrusive impact on a person's quality of life.</p>
      <img src="images/anxiety.jpg" height="600px" width="800px">

    <h2>Types of Anxiety Disorders</h2>
    <p><ul><li>Generalized anxiety disorder (GAD)</li></ul>
    <ul><li>Obsessive-compulsive disorder (OCD)
</li></ul>
    <ul><li>Panic disorder</li></ul></p>
    <h2>Anxiety Disorder Symptoms</h2>
    <p>When you experience the familiar physical and psychological signs of fear and anxiety such as sweating, racing heart, shortness of breath, trembling, worry, or stress, these are cues that something is happening that could be a threat and that you need to deal with it.</p>
    <p>This “flight or fight” reaction activates the physical and psychological resources necessary to deal with the potential danger. Although this system works well most of the time, sometimes it can go into overdrive and do more harm than good. When this happens, it might indicate you have an anxiety disorder.</p>
    
    <h2>Causes</h2>
    
    <p>No one knows exactly what causes anxiety disorders, although there seems to be a variety of factors, including genetics, environment, stress level, brain changes, and trauma. Researchers are discovering more about these links all the time.</p>
    <p>Millions of American adults will experience an anxiety disorder at some point in their lives. Because women are twice as likely as men to experience anxiety disorders, experts recommend that women and girls over the age of 13 should be screened for anxiety during routine health exams</p>
    
    <h2>Diagnosis</h2>
    
    <p>There are no lab tests that can be done in order to diagnose an anxiety disorder, though your doctor may perform some tests to rule out physical problems. Your doctor may refer you to a mental health professional, such as a psychiatrist, a psychologist, or a counselor, who will use specific diagnostic tools and questions to help determine what sort of disorder you may have. </p>
    <h2>Treatments</h2>
    <p>Anxiety disorders can be treated with a variety of options, including psychotherapy, medications, and coping strategies. One particularly effective form of psychotherapy for anxiety disorder sufferers is cognitive behavioral therapy (CBT). If you have an anxiety disorder, there are plenty of treatment options available to help you live your life to the fullest.</p>
    <p>Remember, treatment can take time before you and your physician discover the best options for you. Be patient and keep communication open with your mental health professional in order to figure out the plan best tailored to your individual needs. </p>
   
