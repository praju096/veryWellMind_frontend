    <h1>Depression</h1>
    <p>Clinically significant depression is marked by persistent sadness, a depressed mood, diminished interest in previously enjoyable activities, and other symptoms that interfere with the ability to function in daily life.</p>
    <img src="images/brainhealth.jpg" height="300px" width="300px">
    <h2>What is Depression?</h2>
    <p>The condition often arises from a complex combination of factors that can include genetics, family history, trauma, stress, and illness. It is one of the most common mental disorders, impacting an estimated 7.1% of all adults in the U.S. each year.</p>
    
    <p>Fortunately, effective treatments are available including medications, psychotherapy, or a combination of the two. Self-help strategies can also help people better cope with their symptoms and begin to feel better.</p>
   
    
    <h2>Causes</h2>
    
    <p>Depression is one of the most common mental disorders in the United States. It can affect anyone at almost any age, but what causes depression in some people is not always known. Potential depression causes can include genetics, brain chemistry, life events, medical conditions, and lifestyle factors.</p>
    <p>It is estimated that 10% to 15% of the general population will experience clinical depression in their lifetime. The World Health Organization estimates 5% of men and 9% of women experience depressive disorders in any given year.</p>
     <p>This article discusses common causes of depression. It covers the genetic, biological, and environmental factors that can play a role in the condition.</p>
    <h2>Diagnosis</h2>
    
    <p>As is the case with each new edition of the DSM, there were some changes made to the diagnostic criteria for certain disorders from the DSM-IV to DSM-5, including depression. Some disorders have been removed, while others have been modified or added. </p>
    <p>The following is a look at how depression is diagnosed including an overview of the changes that came with the DSM-5 and what those changes mean for healthcare providers and patients.</p>
    <h2>Self-Checks/At-Home Testing</h2>
    <p>If you are concerned that you may be experiencing symptoms of depression, it's essential that you reach out to a medical or mental health professional.</p>
