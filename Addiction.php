    <h1>Addiction</h1>
    <p>An addiction is a disorder characterized by the compulsive use of a rewarding substance or activity despite experiencing adverse consequences. This complex condition is influenced by a person’s genes and their environment and is often considered a brain disease.</p>
    <img src="images/addiction.jpg" height="600px" width="800px">

    <h2>What is Addiction?</h2>
    <p>In the past, addiction was thought to only encompass substance abuse, but the definition has been expanded to include activities like gambling as well as gaming and shopping. It’s also important to note that addiction is about the underlying neurology of the brain and not about the outward behaviors. With appropriate treatment, people with addictions can go on to live happy, healthy lives.</p>
    
   
    
    <h2>Is Addiction a Disease?</h2>
    
    <p>Addiction is a treatable, chronic disease that affects the brain’s reward, motivation, memory, and related circuitry. Once the brain’s reward system has been impacted by the addictive substance or activity, it can trigger a behavioral and biological response that promotes repeated engagement in the addictive behavior despite harmful consequences.</p>
    <h2>What causes addiction?</h2>
    <p>People engage in potentially addictive activities to feel pleasure, and over time, the reinforcing effect of these activities may change brain chemistry to create an unconscious drive to continue the behavior. Multiple factors influence this process, including genetics, diet, mental health, past traumas, pain, stress, and stage of life.</p>
    
    <h2>How can You Break An Addiction?</h2>
    
    <p>The first step in overcoming addiction is to desire change and avoid the people, places and things which promote the addiction. Support groups, mentors, and 12 step programs provide help, while counselors, recovery centers, and physicians provide expertise and medication to treat cravings and the effects of withdrawal. Eating healthy and getting exercise also play an important role in recovery.</p>
    <h2>Is Addiction Genetic?</h2>
    <p>A person's genetics account for about half of their risk for developing an addiction. The impact of genetics is influenced by an interaction between a person's environment, diet, and biology.  Alcoholism, like other addictions, tends to run in families, and members of those families are at increased risk for developing alcoholism</p>
    
