    <h2>Attention-Deficit/Hyperactivity Disorder</h2>
    <p>Attention-deficit/hyperactivity disorder (ADHD) is a common neurobehavioral condition that is usually first diagnosed during childhood. More than six million children between the ages of two and 17 have been diagnosed with ADHD.It is characterized by patterns of inattention, hyperactivity, and impulsivity that make it difficult for people to pay attention and control their behaviors.</p>
    
    <h2>Types</h2>
    <p>There are three different presentations for ADHD:</p>
    
    <ul><li>Combined presentation</li></ul>
    <ul><li>Predominantly inattentive presentation
</li></ul>
    <ul><li>Predominantly hyperactive-impulsive presentation</li></ul>
